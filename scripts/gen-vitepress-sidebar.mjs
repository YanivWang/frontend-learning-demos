#!/usr/bin/env node
/**
 * 从 manifest.json 生成 VitePress sidebar 与 Demo 索引页。
 *
 * 运行：
 *   node scripts/gen-vitepress-sidebar.mjs
 *   node scripts/gen-vitepress-sidebar.mjs --check
 */

import { readFile, writeFile, mkdir } from "node:fs/promises";
import { join } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = join(fileURLToPath(import.meta.url), "..", "..");
const args = new Set(process.argv.slice(2));
const isCheckMode = args.has("--check");

for (const arg of args) {
  if (arg !== "--check") {
    console.error("用法: node scripts/gen-vitepress-sidebar.mjs [--check]");
    process.exit(1);
  }
}

const OUT_SIDEBAR = join(ROOT, "docs/.vitepress/sidebar.generated.mts");
const OUT_DEMOS_INDEX = join(ROOT, "docs/demos/index.md");

const SECTION_DESC = {
  JavaScript: "纯 JS 知识点 demo（按 01–09 编号分类）",
  CSS: "布局 / 动画 / 视觉 / 响应式 / 现代特性",
  "Vue 2": "响应式原理、基础语法、组件、路由与状态",
  "Vue 3": "基础语法、响应式、组件通信、内置组件、路由状态、原理性能",
  "React 18": "函数组件 33 + 类组件 19：JSX、Hooks、setState、生命周期、面试考点",
  "综合 Demo": "拖拽 / ViewPager 等小项目",
  TypeScript: "基础语法、进阶类型、工程配置、Vue3/React 组件类型",
};

function groupLabel(path) {
  const parts = path.split("/");
  return parts[parts.length - 1] || path;
}

function demoLink(href) {
  return `/${href}`;
}

function buildSidebar(manifest) {
  const guideSection = {
    text: "指南",
    items: [
      { text: "快速开始", link: "/guide/getting-started" },
      { text: "学习路线", link: "/guide/learning-path" },
      { text: "命名约定", link: "/guide/conventions" },
      { text: "贡献指南", link: "/guide/contributing" },
    ],
  };

  const demoSections = manifest.sections.map((section) => ({
    text: section.title,
    collapsed: true,
    items: section.groups.map((group) => ({
      text: groupLabel(group.path),
      collapsed: true,
      items: group.items.map((item) => ({
        text: item.title,
        link: demoLink(item.href),
      })),
    })),
  }));

  return [guideSection, { text: "Demo 索引", link: "/demos/" }, ...demoSections];
}

function renderSidebarFile(sidebar) {
  const body = JSON.stringify(sidebar, null, 2);
  return `// AUTO-GENERATED by scripts/gen-vitepress-sidebar.mjs — do not edit
import type { DefaultTheme } from "vitepress";

const sidebar: DefaultTheme.SidebarItem[] = ${body};

export default sidebar;
`;
}

function escapeMdInline(text) {
  return String(text).replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;");
}

function renderDemosIndex(manifest) {
  const total = manifest.sections.reduce(
    (sum, section) =>
      sum + section.groups.reduce((groupSum, group) => groupSum + group.items.length, 0),
    0
  );

  const lines = [
    "<!-- AUTO-GENERATED by scripts/gen-vitepress-sidebar.mjs — do not edit -->",
    "",
    "# Demo 索引",
    "",
    `共 **${total}** 个可运行 demo。点击链接打开对应 HTML 页面。`,
    "",
  ];

  for (const section of manifest.sections) {
    lines.push(`## ${section.title}`, "");
    if (SECTION_DESC[section.title]) {
      lines.push(SECTION_DESC[section.title], "");
    }
    for (const group of section.groups) {
      lines.push(`### ${groupLabel(group.path)}`, "");
      for (const item of group.items) {
        const theme = item.theme ? ` — ${escapeMdInline(item.theme)}` : "";
        lines.push(`- [${escapeMdInline(item.title)}](${demoLink(item.href)})${theme}`);
      }
      lines.push("");
    }
  }

  return lines.join("\n");
}

async function main() {
  const manifestRaw = await readFile(join(ROOT, "manifest.json"), "utf8");
  const manifest = JSON.parse(manifestRaw);
  const sidebar = buildSidebar(manifest);
  const sidebarContent = renderSidebarFile(sidebar);
  const demosIndexContent = renderDemosIndex(manifest);

  if (isCheckMode) {
    const currentSidebar = await readFile(OUT_SIDEBAR, "utf8");
    const currentDemosIndex = await readFile(OUT_DEMOS_INDEX, "utf8");
    const changed =
      currentSidebar !== sidebarContent || currentDemosIndex !== demosIndexContent;
    if (changed) {
      console.error(
        "[gen-vitepress-sidebar] 失败：sidebar / demos 索引与 manifest 不一致，请运行 node scripts/gen-vitepress-sidebar.mjs"
      );
      process.exitCode = 1;
      return;
    }
    console.log("[gen-vitepress-sidebar] 通过：VitePress 导航已同步");
    return;
  }

  await mkdir(join(ROOT, "docs/demos"), { recursive: true });
  await mkdir(join(ROOT, "docs/.vitepress"), { recursive: true });
  await writeFile(OUT_SIDEBAR, sidebarContent, "utf8");
  await writeFile(OUT_DEMOS_INDEX, demosIndexContent, "utf8");
  console.log(
    `[gen-vitepress-sidebar] 已生成 sidebar.generated.mts 与 demos/index.md（${manifest.sections.length} 个分类）`
  );
}

main().catch((err) => {
  console.error("[gen-vitepress-sidebar] 失败：", err);
  process.exitCode = 1;
});
