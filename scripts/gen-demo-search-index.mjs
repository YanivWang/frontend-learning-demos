#!/usr/bin/env node
/**
 * 从 manifest.json 生成 VitePress 可搜索的 Demo 摘要页（含要点 keywords）。
 * 运行：node scripts/gen-demo-search-index.mjs [--check]
 */

import { readFile, writeFile, mkdir } from "node:fs/promises";
import { join } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = join(fileURLToPath(import.meta.url), "..", "..");
const OUT = join(ROOT, "docs/demos/search-index.md");
const isCheckMode = process.argv.includes("--check");

function escapeMd(text) {
  return String(text)
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/\{/g, "&#123;")
    .replace(/\}/g, "&#125;")
    .replace(/\|/g, "\\|");
}

async function main() {
  const manifest = JSON.parse(await readFile(join(ROOT, "manifest.json"), "utf8"));
  const lines = [
    "<!-- AUTO-GENERATED by scripts/gen-demo-search-index.mjs — do not edit -->",
    "",
    "# Demo 搜索索引",
    "",
    "本页汇总各 demo 的**主题**与**要点关键词**，供 VitePress 本地搜索索引。",
    "",
  ];

  for (const section of manifest.sections) {
    lines.push(`## ${section.title}`, "");
    for (const group of section.groups) {
      lines.push(`### ${group.path.split("/").pop()}`, "");
      for (const item of group.items) {
        const link = `/${item.href}`;
        lines.push(
          `- [${escapeMd(item.title)}](${link}) — ${escapeMd(item.theme || "")}`
        );
      }
      lines.push("");
    }
  }

  const content = lines.join("\n");

  if (isCheckMode) {
    const current = await readFile(OUT, "utf8");
    if (current !== content) {
      console.error("[gen-demo-search-index] 失败：search-index.md 不同步");
      process.exitCode = 1;
      return;
    }
    console.log("[gen-demo-search-index] 通过");
    return;
  }

  await mkdir(join(ROOT, "docs/demos"), { recursive: true });
  await writeFile(OUT, content, "utf8");
  console.log("[gen-demo-search-index] 已生成 docs/demos/search-index.md");
}

main().catch((err) => {
  console.error("[gen-demo-search-index] 异常：", err);
  process.exitCode = 1;
});
